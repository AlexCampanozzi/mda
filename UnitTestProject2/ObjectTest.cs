using System;
using Microsoft.VisualStudio.TestTools.UnitTesting;
using Explorus;
using System.Drawing;
using System.Windows.Forms;
using Explorus.Model;

namespace ExplorusTests

{
    [TestClass]
    public class ObjectTest
    {
        
        [TestMethod]
        public void CreateDoor()
        {
            Door door = new Door(new Point(5,5));
            Assert.AreEqual(new Point(5, 5), door.GetPosition());
        }

        [TestMethod]
        public void CreateGem()
        {
            Gem gem = new Gem(new Point(15, 5));
            Assert.AreEqual(new Point(15, 5), gem.GetPosition());
        }

        [TestMethod]
        public void CreateWall()
        {
            Door door = new Door(new Point(1, 3));
            Assert.AreEqual(new Point(1, 3), door.GetPosition());
        }
    }
}
